#!/usr/bin/perl

######################################################################
# 基本設定
######################################################################
use strict;
use warnings;

use Common_pl::Unix::Mod_num2str;

######################################################################
# 変数定義
######################################################################
my @modes;
my $mode;

######################################################################
# サブルーチン定義
######################################################################
sub TEST_FILE_TYPE {
	@modes = (
	0000000, 0010000, 0020000, 0030000, 0040000, 0050000, 0060000, 0070000,
	0100000, 0110000, 0120000, 0130000, 0140000, 0150000, 0160000, 0170000, 
	);

	print "########################################\n";
	print "# File types\n";
	print "########################################\n";
	foreach $mode (@modes) {
		printf("%06o %-10s\n", $mode, MOD_NUM2STR($mode));
	}
	print "\n";
}

sub TEST_PERMISSION {
	@modes = (
	0100000,
	0100001, 0100002, 0100004, 0100007,
	0100010, 0100020, 0100040, 0100070,
	0100100, 0100200, 0100400, 0100700,
	0101000, 0102000, 0104000, 0107000,
	0101001, 0102010, 0104100, 0107111,
	0107777,
	);

	print "########################################\n";
	print "# Permissions\n";
	print "########################################\n";
	foreach $mode (@modes) {
		printf("%06o %-10s\n", $mode, MOD_NUM2STR($mode));
	}
	print "\n";
}

######################################################################
# メインルーチン
######################################################################

TEST_FILE_TYPE();
TEST_PERMISSION();

